def _build_handoff_payload(*, step_graph: StepGraph, class_name: str, test_name: str) -> dict[str, Any]:
    steps: list[dict[str, Any]] = []
    for idx, step in enumerate(step_graph.steps, start=1):
        steps.append(
            {
                "stepId": step.step_id,
                "action": step.action,
                "methodName": _method_name(step.action, idx),
                "pageClass": class_name,
                "locators": _build_locator_map_for_step(step),
                "metadata": _step_data(step),
            }
        )
    return {
        "formatVersion": "1.0",
        "runId": step_graph.run_id,
        "testName": test_name,
        "pageClass": class_name,
        "steps": steps,
    }


def _infer_target_url(step_graph: StepGraph) -> str | None:
    for step in step_graph.steps:
        if step.action.strip().lower() != "navigate":
            continue
        metadata = _step_data(step)
        url = _pick_string(metadata, "url", "targetUrl", "navigateUrl", "sourceUrl")
        if url:
            return url
    return None


def _has_recorded_navigation(step_graph: StepGraph) -> bool:
    return any(step.action.strip().lower() == "navigate" for step in step_graph.steps)


def _build_locator_map(step_graph: StepGraph) -> dict[str, list[str]]:
    locator_map: dict[str, list[str]] = {}
    for step in step_graph.steps:
        if step.target is None:
            continue
        selectors = [step.target.primary_selector] + list(step.target.fallback_selectors)
        deduped: list[str] = []
        seen: set[str] = set()
        for selector in selectors:
            value = selector.strip()
            if not value or value in seen:
                continue
            seen.add(value)
            deduped.append(value)
        if deduped:
            locator_map[step.step_id] = deduped
    return locator_map


def _pick_string(metadata: dict[str, Any], *keys: str) -> str | None:
    for key in keys:
        value = metadata.get(key)
        if isinstance(value, list):
            for item in value:
                if isinstance(item, str) and item.strip():
                    return item.strip()
        if isinstance(value, str) and value.strip():
            return value.strip()
    return None


def _pick_int(metadata: dict[str, Any], *keys: str) -> int | None:
    for key in keys:
        value = metadata.get(key)
        if isinstance(value, int):
            return value
        if isinstance(value, str) and value.strip().isdigit():
            return int(value.strip())
    return None


def _pick_bool(metadata: dict[str, Any], *keys: str, default: bool) -> bool:
    for key in keys:
        if key not in metadata:
            continue
        value = metadata[key]
        if isinstance(value, bool):
            return value
        if isinstance(value, str):
            lowered = value.strip().lower()
            if lowered in {"true", "1", "yes"}:
                return True
            if lowered in {"false", "0", "no"}:
                return False
    return default


def _step_data(step: Step) -> dict[str, Any]:
    metadata = step.metadata if isinstance(step.metadata, dict) else {}
    flattened: dict[str, Any] = {}
    params = metadata.get("params")
    if isinstance(params, dict):
        flattened.update(params)
    flattened.update(metadata)
    return flattened


def _build_locator_map_for_step(step: Step) -> list[str]:
    if step.target is None:
        return []
    selectors = [step.target.primary_selector, *step.target.fallback_selectors]
    out: list[str] = []
    seen: set[str] = set()
    for selector in selectors:
        clean = selector.strip()
        if not clean or clean in seen:
            continue
        seen.add(clean)
        out.append(clean)
    return out


def _render_locator_map_with_labels(
    step_graph: StepGraph,
    locator_map: dict[str, list[str]],
) -> list[str]:
    """Render the LOCATORS map with a rich comment above each entry."""
    step_index: dict[str, tuple[int, Step]] = {}
    for idx, step in enumerate(step_graph.steps, start=1):
        step_index[step.step_id] = (idx, step)

    lines: list[str] = []
    for step_id, selectors in locator_map.items():
        if step_id in step_index:
            idx, step = step_index[step_id]
            action = step.action.strip().lower()
            hint = _element_hint(step)
            comment = f"Step {idx} — {action}"
            if hint:
                comment += f" | {hint}"
        else:
            comment = step_id
        selectors_json = json.dumps(selectors, ensure_ascii=True)
        lines.append(f"  // {comment}")
        lines.append(f"  {json.dumps(step_id)}: {selectors_json},")
    return lines


def _render_step_comment(*, step: Step, step_number: int, indent: str) -> list[str]:
    """Multi-line comment block above each step — useful for humans and LLM refactoring."""
    action = step.action.strip().lower()
    hint = _element_hint(step)
    metadata = _step_data(step)

    # Line 1: step number, action, element identity
    first = f"Step {step_number} — {action}"
    if hint:
        first += f" | {hint}"

    # Line 2: page URL context (helps LLM decide which Page class this belongs to)
    page_url = _pick_string(metadata, "pageUrl", "sourceUrl", "url") or ""
    # for navigate steps the url IS the target, not the page context — skip it there
    if action == "navigate":
        page_url = ""

    lines = [f"{indent}// {first}"]
    if page_url:
        lines.append(f"{indent}// page: {page_url}")

    # Line 3: reasoning hint from locator engine (explains why this selector was chosen)
    if step.target and step.target.reasoning_hint:
        lines.append(f"{indent}// locator note: {step.target.reasoning_hint}")

    return lines


def _element_hint(step: Step) -> str:
    """Short human-readable element identity string from descriptor metadata."""
    metadata = _step_data(step)
    descriptor: dict[str, Any] = metadata.get("descriptor") or {}

    parts: list[str] = []
    tag = descriptor.get("tag", "")
    role = descriptor.get("role", "")
    aria = descriptor.get("ariaLabel", "")
    testid = descriptor.get("testid", "")
    placeholder = descriptor.get("placeholder", "")
    text = descriptor.get("text", "")
    input_type = descriptor.get("inputType", "")
    el_id = descriptor.get("id", "")

    if tag:
        label = tag
        if input_type:
            label += f"[type={input_type}]"
        elif role:
            label += f"[role={role}]"
        parts.append(label)

    if aria:
        parts.append(f'aria-label="{aria}"')
    elif testid:
        parts.append(f'data-testid="{testid}"')
    elif placeholder:
        parts.append(f'placeholder="{placeholder}"')
    elif el_id:
        parts.append(f'id="{el_id}"')
    elif text:
        snippet = text[:40].strip()
        if snippet:
            parts.append(f'text="{snippet}"')

    return " | ".join(parts)


def _method_name(action: str, step_number: int) -> str:
    safe = re.sub(r"[^a-zA-Z0-9_]+", "_", (action or "step").strip().lower()).strip("_")
    safe = safe or "step"
    return f"step{step_number}_{safe}"


def _slugify(value: str) -> str:
    slug = re.sub(r"[^a-zA-Z0-9]+", "-", (value or "").strip().lower()).strip("-")
    return slug


def _ts_string(value: str) -> str:
    escaped = (
        value.replace("\\", "\\\\")
        .replace("'", "\\'")
        .replace("\n", "\\n")
        .replace("\r", "\\r")
        .replace("\t", "\\t")
    )
    return f"'{escaped}'"
